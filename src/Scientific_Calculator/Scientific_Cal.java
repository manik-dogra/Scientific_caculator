    private void jbtnConvertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnConvertActionPerformed
        if (jtxtConvert.getText().equals(""))
        {
            jLabelResult.setText("Enter the value");
            
        }
        else if (jCDistance.getSelectedItem().equals("Select an option"))
        {
            jLabelResult.setText("Select an option");
        }
        
        else if (jCDistance.getSelectedItem().equals("Distance"))
        {
            double dst1 = Double.parseDouble(jtxtConvert.getText());
            double dst2 = Double.parseDouble(jtxttoConvert.getText());
            double dst3;
                // Distance = Speed * Time
                jLabel1.setText("Distance = ");
                jLabel2.setText("Speed  X");
                jLabel3.setText("Time");
                dst3 = dst2 * dst1;
                String value = String.format("%.2f", dst3);
                jLabelResult.setText(value);      
        }
        else if (jCDistance.getSelectedItem().equals("Speed"))
        {
            double dst1 = Double.parseDouble(jtxtConvert.getText());
            double dst2 = Double.parseDouble(jtxttoConvert.getText());
            double dst3;
                // Speed = Distance / Time
                jLabel1.setText("Speed = ");
                jLabel2.setText("Distance /");
                jLabel3.setText("Time");
                dst3 = dst1 / dst2;
                String value = String.format("%.2f", dst3);
                jLabelResult.setText(value);      
        }
        else if (jCDistance.getSelectedItem().equals("Time"))
        {
            double dst1 = Double.parseDouble(jtxtConvert.getText());
            double dst2 = Double.parseDouble(jtxttoConvert.getText());
            double dst3;
                // Time = Distance * Speed
                jLabel1.setText("Time = ");
                jLabel2.setText("Distance /");
                jLabel3.setText("Speed");
                dst3 = (dst2 * dst1) / dst1;
                String value = String.format("%.2f", dst3);
                jLabelResult.setText(value);      
        }
    }//GEN-LAST:event_jbtnConvertActionPerformed

