    private void jbtnConvertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnConvertActionPerformed
        if (jtxtConvert.getText().equals(""))
        {
            jlblConvert.setText("Enter value");   
        }
         
        else if (jcmbCurrency.getSelectedItem().equals("Choose One..."))
        {
            jlblConvert.setText("Choose country");
        }
        
        double indian_INR = Double.parseDouble(jtxtConvert.getText());

        
        if (jcmbCurrency.getSelectedItem().equals("Nigeria"))
        {
            String CurrencyConv = String.format("N %.2f", indian_INR * Nigeria);
            jlblConvert.setText(CurrencyConv);
        }
        
        else if (jcmbCurrency.getSelectedItem().equals("USA"))
        {
            String CurrencyConv = String.format("$ %.2f", indian_INR * US_Dollar);
            jlblConvert.setText(CurrencyConv);
        }
        
        else if (jcmbCurrency.getSelectedItem().equals("Brazil"))
        {
            String CurrencyConv = String.format("R$ %.2f", indian_INR * Brazil);
            jlblConvert.setText(CurrencyConv);
        }
        
        else if (jcmbCurrency.getSelectedItem().equals("Canada"))
        {
            String CurrencyConv = String.format("C$ %.2f", indian_INR * Canadian_Dollar);
            jlblConvert.setText(CurrencyConv);
        }
        
        else if (jcmbCurrency.getSelectedItem().equals("Russia"))
        {
            String CurrencyConv = String.format("â‚½ %.2f", indian_INR * Russia);
            jlblConvert.setText(CurrencyConv);
        }
        
        else if (jcmbCurrency.getSelectedItem().equals("Kenyan"))
        {
            String CurrencyConv = String.format("KES %.2f", indian_INR * Kenyan);
            jlblConvert.setText(CurrencyConv);
        }
        
        else if (jcmbCurrency.getSelectedItem().equals("Indonesia"))
        {
            String CurrencyConv = String.format("Idr %.2f", indian_INR * Indonesia);
            jlblConvert.setText(CurrencyConv);
        }
        
        else if (jcmbCurrency.getSelectedItem().equals("Philippine"))
        {
            String CurrencyConv = String.format("PhP %.2f", indian_INR * Philippine);
            jlblConvert.setText(CurrencyConv);
        }
        
        else if (jcmbCurrency.getSelectedItem().equals("Oman"))
        {
            String CurrencyConv = String.format("OMR %.2f", indian_INR * Oman);
            jlblConvert.setText(CurrencyConv);
        }
        
        else if (jcmbCurrency.getSelectedItem().equals("Dubai"))
        {
            String CurrencyConv = String.format("AED %.2f", indian_INR * Dubai);
            jlblConvert.setText(CurrencyConv);
        }
        
        else if (jcmbCurrency.getSelectedItem().equals("Saudi Arabia"))
        {
            String CurrencyConv = String.format("SAR %.2f", indian_INR * Saudi_Arabia);
            jlblConvert.setText(CurrencyConv);
        }
        
        
        
        
    }//GEN-LAST:event_jbtnConvertActionPerformed

